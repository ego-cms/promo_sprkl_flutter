import 'package:flutter/material.dart';

class SupportingColors {
  final Color? lime;
  final Color? emerald;
  final Color? teal;
  final Color? cyan;
  final Color? sky;
  final Color? indigo;
  final Color? violet;
  final Color? purple;
  final Color? fuchsia;
  final Color? pink;

  SupportingColors({
    required this.lime,
    required this.emerald,
    required this.teal,
    required this.cyan,
    required this.sky,
    required this.indigo,
    required this.violet,
    required this.purple,
    required this.fuchsia,
    required this.pink,
  });
}
